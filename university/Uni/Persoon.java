package Uni;

public class Persoon {
	
	String name;
	String pas;
	int id;
	String lastname;
	String birthday;
	int age;
	
	Persoon(String name, String pas, int id, String lastname, String birthday, int age)
	{
		this.name = name;
		this.pas = pas;
		this.id = id;
		this.lastname = lastname;
		this.birthday = birthday;
		this.age = age;
	}

}
